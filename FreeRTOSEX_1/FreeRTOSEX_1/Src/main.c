/* LED1_TASK function */
void LED1_TASK(void const * argument)
{
  /* USER CODE BEGIN LED1_TASK */
  /* Infinite loop */
  for(;;)
  {
    count1++;
    if(switch1 == 0)            //200ms toggle for 5secs. switch1 = 0
    {
      HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_6);
    }
    else                        //400ms toggle for 5secs. switch1 = 1
    {
      if(temp1 == 1)
      {
        temp1 = 0;
        HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_6);
      }
      else
        temp1++;
      if(count1 == 25)
      {
        switch2 = 1;
        osThreadResume(LED2Handle);
      }
    }
    if(count1 == 25)
    {
      count1 = 0;
      if(switch2 != 1)
        osThreadSuspend(osThreadGetId());
    }
    osDelay(200);
  }
  /* USER CODE END LED1_TASK */
}

/* LED2_TASK function */
void LED2_TASK(void const * argument)
{
  /* USER CODE BEGIN LED2_TASK */
  /* Infinite loop */
  for(;;)
  {
    count2++;
    if(switch2 == 1)
    {
      switch2 = 0;
      switch1 = 0;
    }
    HAL_GPIO_TogglePin(GPIOA, GPIO_PIN_7);
    if(count2 == 20)
    {
      count2 = 0;
      switch1 = 1;
      osThreadResume(LED1Handle);
      osThreadSuspend(osThreadGetId());
    }
    osDelay(500);
  }
  /* USER CODE END LED2_TASK */
}

